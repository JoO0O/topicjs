switch(value) {
	// Блок операторов
	case "1": // При вводе 1..
	case "2": // 2..
	case "3": // 3..
		document.write("Hello!"); // Выводится ответ : "Hello!"
	break;
	// Блок операторов
	case "4": // При вводе 4..
	case "5": // 5..
		document.write("World"); // Выводится ответ : "World" 
	break;
	// Блок операторов
	default:
		document.write("error"); // error , если >5 or 0



}
