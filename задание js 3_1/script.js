var a = prompt("Введите чило 11", "0");
// Если переменная "а" введена верно, то выведется ответ "Введённое значение верно"
if (a == 11){
	document.write("Введённое значение верно");
}
// Если переменная "а" введена неверно, то выведется ответ "Введено значение неверно"
else {
	document.write("Введено значение неверно")
}

